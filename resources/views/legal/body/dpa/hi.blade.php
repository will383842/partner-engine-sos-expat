<p>प्रसंस्करणकर्ता निम्नलिखित के लिए प्रतिबद्ध है:</p>
<ol>
    <li>केवल नियंत्रक के दस्तावेजी निर्देश पर डेटा संसाधित करें, कानूनी बाध्यता को छोड़कर;</li>
    <li>डेटा की गोपनीयता की गारंटी दें और केवल गोपनीयता प्रतिबद्धता वाले कर्मचारियों को
        उन तक पहुँच प्रदान करें;</li>
    <li>उचित तकनीकी और संगठनात्मक उपायों को लागू करें (पारगमन में TLS 1.3 एन्क्रिप्शन,
        विश्राम में एन्क्रिप्शन, भूमिका-आधारित पहुँच नियंत्रण, पहुँच लॉग, भू-अनावश्यक एन्क्रिप्टेड
        बैकअप, वार्षिक पहुँच समीक्षा);</li>
    <li>उचित सीमा तक नियंत्रक की प्रभाव आकलन (DPIA) के संचालन में, डेटा विषयों के अनुरोधों के
        प्रबंधन में, और डेटा उल्लंघन की सूचना में सहायता करें;</li>
    <li>किसी भी डेटा उल्लंघन को इसकी खोज से अधिकतम <strong>72 घंटे</strong> के भीतर ईमेल द्वारा
        बिलिंग पते और साझेदार के DPO (यदि नामित हो) को सूचित करें, GDPR के अनुच्छेद 33-34 के अनुसार;</li>
    <li>नियंत्रक की ओर से किए गए प्रसंस्करण गतिविधियों का रिकॉर्ड बनाए रखें (GDPR अनुच्छेद 30)।</li>
</ol>

