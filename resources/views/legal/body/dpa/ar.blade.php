بالإضافة إلى جهات اتصال الفوترة والمسؤولين الذين يعينهم الشريك.</p>

<h2>4. التزامات المعالج</h2>
<p>يلتزم المعالج بما يلي:</p>
<ol>
    <li>عدم معالجة البيانات إلا بناءً على تعليمات موثقة من المسؤول عن المعالجة، باستثناء
        الالتزامات القانونية.</li>
    <li>ضمان سرية البيانات وعدم السماح بالوصول إليها إلا للموظفين الملتزمين بالسرية.</li>
    <li>تنفيذ تدابير تقنية وتنظيمية مناسبة (تشفير TLS 1.3 في الإرسال، تشفير في حالة السكون،
        التحكم في الوصول حسب الأدوار، تسجيل عمليات الوصول، نسخ احتياطية مشفرة جغرافياً،
        مراجعة سنوية للوصول).</li>
    <li>مساعدة المسؤول عن المعالجة، بقدر معقول، في إجراء تقييمات الأثر، ومعالجة طلبات الأشخاص
        المعنيين، وإخطار انتهاكات البيانات.</li>
    <li>الإخطار بأي انتهاك للبيانات في غضون <strong>72 ساعة</strong> كحد أقصى من اكتشافه،
        عبر البريد الإلكتروني إلى عنوان الفوترة وإلى DPO الشريك إن وُجد، وفقاً للمادتين
        33-34 من RGPD.</li>
    <li>الاحتفاظ بسجل لأنشطة المعالجة المنفذة لحساب المسؤول عن المعالجة (المادة 30 من RGPD).</li>
</ol>

