ينص عليه RGPD وتعترف به Andmekaitse Inspektsioon.</p>

<h2>7. حقوق الأشخاص المعنيين</h2>
<p>يبقى المسؤول عن المعالجة نقطة الاتصال الوحيدة للأشخاص المعنيين لممارسة حقوقهم
(الوصول، التصحيح، الحذف، التقييد، الاعتراض، النقل). يقدم المعالج خلال مدة معقولة، بناءً على
طلب مُسبَّب من المسؤول، العناصر اللازمة لتلبية حقوق الأشخاص.</p>

<h2>8. مدة الحفظ</h2>
<ul>
    <li>بيانات التعريف والاتصال: مدة العلاقة التعاقدية + 3 سنوات (التقادم التجاري الإستوني، VÕS §146).</li>
    <li>بيانات الاستخدام والفوترة: 7 سنوات (الالتزامات المحاسبية الإستونية، Raamatupidamise seadus §12).</li>
    <li>سجلات المصادقة التقنية: 12 شهراً.</li>
    <li>بصمات SHA-256 للوثائق الموقعة وأدلة القبول: 10 سنوات من تاريخ التوقيع، وفقاً لمتطلبات eIDAS.</li>
</ul>

<h2>9. حذف البيانات أو إعادتها</h2>
<p>عند انتهاء العقد الرئيسي، يقوم المعالج، حسب اختيار المسؤول المُبلَّغ خلال ثلاثين (30) يوماً:</p>
<ul>
    <li>إما بالحذف النهائي للبيانات في غضون 90 يوماً، باستثناء الالتزامات القانونية للحفظ؛</li>
    <li>أو بإعادتها بصيغة منظمة وشائعة الاستخدام.</li>
</ul>

