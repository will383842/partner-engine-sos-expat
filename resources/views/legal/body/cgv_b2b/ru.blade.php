<h2>5. Обязательства Поставщика</h2>
<p>Поставщик обязуется:</p>
<ul>
    <li>обеспечивать доступ к консоли партнёра и связанному API с целевой ежемесячной
        доступностью 99,5% (за исключением плановых работ, о которых уведомляется за 48 часов);</li>
    <li>обеспечивать каждому Абоненту бесплатный доступ в пределах согласованных квот к
        сервису экстренного звонка адвокату или эксперту;</li>
    <li>гарантировать качество телефонной связи (инфраструктура Twilio, мультирегиональное резервирование);</li>
    <li>обеспечивать конфиденциальность звонков и обработку персональных данных в соответствии
        с прилагаемым DPA.</li>
</ul>

