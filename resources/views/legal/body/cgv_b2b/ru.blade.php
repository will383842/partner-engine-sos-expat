<h2>1. Предмет и стороны</h2>
<p>Настоящие Общие условия продажи («<strong>Условия</strong>») регулируют использование
партнёром <strong>{{ $vars['partner_name'] }}</strong> («<strong>Партнёр</strong>»)
сервиса SOS-Call, предоставляемого <strong>{{ $vars['provider_legal_name'] }}</strong>,
эстонской компанией, зарегистрированной в эстонском Коммерческом регистре (Äriregister)
@if($vars['provider_siret'] ?? null)
    под регистрационным номером <strong>{{ $vars['provider_siret'] }}</strong>,
@endif
@if($vars['provider_address'] ?? null)
    с юридическим адресом {{ $vars['provider_address'] }},
@endif
далее «<strong>SOS-Expat</strong>» или «<strong>Поставщик</strong>».</p>
<p>Сервис SOS-Call позволяет абонентам, зарегистрированным Партнёром, бесплатно совершать
телефонные звонки экстренной юридической помощи к независимым адвокатам и экспертам,
указанным на платформе Поставщика, в обмен на ежемесячную абонентскую плату Партнёра,
рассчитанную в соответствии со статьёй 4.</p>

<h2>2. Определения</h2>
<ul>
    <li><strong>Абонент</strong>: физическое лицо, зарегистрированное Партнёром и пользующееся
        сервисом SOS-Call. Идентифицируется уникальным кодом SOS-Call или подтверждённым
        номером телефона и адресом электронной почты.</li>
    <li><strong>Звонок SOS-Call</strong>: телефонный звонок, совершённый Абонентом через
        платформу SOS-Expat указанному адвокату или эксперту.</li>
    <li><strong>Ежемесячная подписка</strong>: плата, причитающаяся с Партнёра ежемесячно,
        рассчитываемая по параметрам статьи 4.</li>
    <li><strong>Консоль партнёра</strong>: веб-интерфейс, предоставляемый Партнёру по адресу
        <em>partner-engine.sos-expat.com</em> и <em>sos-expat.com/partner/*</em>.</li>
</ul>

<h2>3. Срок действия и расторжение</h2>
<p>Настоящий договор вступает в силу <strong>{{ $vars['starts_at'] }}</strong>
@if($vars['expires_at'])
    и истекает <strong>{{ $vars['expires_at'] }}</strong>, если Стороны не продлят его прямо.
@else
    и заключён на неопределённый срок.
@endif
</p>
<p>Любая Сторона вправе расторгнуть договор в любое время с письменным уведомлением за
тридцать (30) дней, направленным заказным письмом или электронной почтой по адресу
выставления счетов. Расторжение вступает в силу по истечении срока уведомления.
Текущие ежемесячные подписки подлежат оплате до окончания этого срока.</p>
<p>В случае существенного нарушения одной из Сторон своих договорных обязательств другая
Сторона вправе расторгнуть договор в одностороннем порядке в соответствии с
<strong>Эстонским законом об обязательственном праве (Võlaõigusseadus, «VÕS») §116</strong>,
после требования, оставшегося без удовлетворения в течение пятнадцати (15) дней.</p>

