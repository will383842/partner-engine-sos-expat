        SOS-Expat и что их данные обрабатываются в соответствии с прилагаемым DPA;</li>
    <li>не использовать сервис мошеннически или злоупотребляюще (например: перепродажа доступа,
        предоставление кодов SOS-Call неустановленным третьим лицам, обход квот).</li>
</ul>

<h2>7. Ответственность</h2>
<p>Поставщик предоставляет инфраструктуру связи. Он не является стороной правоотношений между
Абонентом и консультирующимся адвокатом или экспертом. Советы и заключения, данные во время
звонка, относятся к исключительной ответственности соответствующего адвоката или эксперта.</p>
<p>С учётом императивных ограничений <strong>VÕS §106</strong>, совокупная ответственность
Поставщика по настоящему договору не может превышать общей суммы ежемесячных подписок,
уплаченных Партнёром в течение двенадцати (12) месяцев, предшествующих событию, повлёкшему
ответственность.</p>
<p>Поставщик не несёт ответственности за любые косвенные убытки, в том числе упущенную
выгоду, потерю оборота, ущерб репутации или потерю данных.</p>

